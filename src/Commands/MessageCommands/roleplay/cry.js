const request = require('node-superfetch');

module.exports = {
    name: 'cry',
    aliases: [],
    description: 'cry',
    ownerOnly: false,
    cooldown: 3000,
    userPermissions: ['SEND_MESSAGES'],
    clientPermissions: ['SEND_MESSAGES', 'EMBED_LINKS'],
    category: 'Roleplay',
    usage: '',
    run: async (client, message, args, container) => {
        try {
            const data = await request.get(`${process.env.API_URL}/api/roleplay/cry`);

            const embed = new container.Discord.MessageEmbed()
                .setColor('#d0d0e3')
                .setDescription(`_${message.author} started crying._`)
                .setImage(data.body.url)
                .setFooter({ text: `Roleplay Commands | Made by Bear#3437 | ©️ ${new Date().getFullYear()} Tamako`, iconURL: client.user.displayAvatarURL({ dynamic: true }) });
    
            return message.reply({ embeds: [embed] });

        } catch (err) {
            return message.reply({ content: `Let my developer know in the support server https://discord.gg/dDnmY56 or using \`${container.Config.prefix[0]}feedback\` command`, embeds: [ 
                new container.Discord.MessageEmbed()
                    .setColor('RED')
                    .setTitle('Error')
                    .setDescription(`\`${err}\``)
                    .setFooter({ text: `Error Occured | Made by Bear#3437 | ©️ ${new Date().getFullYear()} Tamako`, iconURL: client.user.displayAvatarURL({ dynamic: true }) })]
            });

        }
    }

};

